    public void updateNumbers()
    {
        // Set the games played and won Double.toString(player.getGamesPlayed())
        this.playedTxt.setText(new DecimalFormat("###").format(player.getGamesPlayed()));
        this.wonTxt.setText(new DecimalFormat("###").format(player.getGamesWon()));
        
        double percentage = (player.getGamesWon()/player.getGamesPlayed())*100;
        this.percentageTxt.setText(new DecimalFormat("###").format(percentage) + "%");
    }
