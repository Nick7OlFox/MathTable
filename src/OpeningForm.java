    private void logInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInBtnActionPerformed
        // This button is used to call the methods necessary for the user to log in

        // First we need to retrieve the password the user introduced and convert it into a String
        String passText = new String(passwordTxtLogInPanel.getPassword());

        // Then we create a new player object, where we store the username and the string password
        player = new Player(usernameTxtLogInPanel.getText(), passText);

        // Finally once we get the player object ready, we call the logIn method on the Database class to check the password and, if it matches, retrieve the player information
        // This method is going to return a boolean value. If it returns true then it means we can proccedd to make the necessary adjustments to the game
        if(db.logIn(player))
        {
            isLogged = true;
            presentLogIn();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "The Program wasn't able to log you in.");
        }

        // Finally we go back to the main window
        CardLayout card = (CardLayout)mainPanel.getLayout();
        card.show(mainPanel, "openingMenuPanel");
    }//GEN-LAST:event_logInBtnActionPerformed

