    private void checkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkBtnActionPerformed
        // Since this button is used for both checking and advancing to the next level we have to first check what the button is gonna do
        if(this.checkBtn.getText().equals("Check Answers"))
        {
            // Add one to the games played
            if(isLogged)
            {
                player.gamePlayed();
            }

            // To make sure the program doesnt crash, we use a try catch so that if it is empty it'll give an error when creating the object
            try
            {
                // When the player presses the check answers button we first have to creat a level object with the user data to use to compare to the actual level
                int n1 = Integer.parseInt(number1Square.getText());
                int n2 = Integer.parseInt(number2Square.getText());
                int n3 = Integer.parseInt(number3Square.getText());
                int n4 = Integer.parseInt(number4Square.getText());
                int n5 = Integer.parseInt(number5Square.getText());
                int n6 = Integer.parseInt(number6Square.getText());
                int n7 = Integer.parseInt(number7Square.getText());
                int n8 = Integer.parseInt(number8Square.getText());
                int n9 = Integer.parseInt(number9Square.getText());
                int n10 = Integer.parseInt(number10Square.getText());
                int n11 = Integer.parseInt(number11Square.getText());
                int n12 = Integer.parseInt(number12Square.getText());
                int n13 = Integer.parseInt(number13Square.getText());
                int n14 = Integer.parseInt(number14Square.getText());
                int n15 = Integer.parseInt(number15Square.getText());

                // The player level object does not need any ID, difficulty, or sign, just the number values
                Level playerLevel = new Level("","","", n1, n2, n3, n4, n5, n6, n7, n8, n9, n10, n11, n12, n13, n14, n15);

                // Now that we have the player object, we can call the checkAnswers method in the actual level object
                if(level.checkAnswers(playerLevel, this))
                {
                    // If the answers the player introduced were correct, then we display congrats message and give the player the option to play the next level
                    this.levelInformationLabel.setText("<html>CONGRATs<br/>You can now proceed to the next level!</html>");
                    if(isLogged)
                    {
                        player.gameWon();
                    }
                    
                    // Now we need to unlock the next level
                    unlockLevel(level.getLevelID());
                    
                    // Finally we change the text on the button
                    this.checkBtn.setText("Next Level");
                    
                }
                else
                {
                    // If there is at least one wrong answer, then we give the player the option to replay the level and update the information
                    this.levelInformationLabel.setText("<html>OH NO!<br/>Some of your answers are wrong.</html>");
                    this.checkBtn.setText("Replay");
                }
            }
            catch(Exception e)
            {
                // If the player hasn't introduced all of the number needed, then we give a message asking the player to introduce a number on every space
                this.levelInformationLabel.setText("<html>Make sure you fill every space before checking the answers!</html>");
            }
            
            if(isLogged)
            {
                updateNumbers();
            }
        }
        else if(this.checkBtn.getText().equals("Replay"))
        {
            // If the player choses to replay the level all we have to do is prepare the window for the player again
            level.PrepareWindow(this);
        }
        
        else
        {
            // If the player choses to play the next level them all we have to do is do prepare the window for the player to see
            // The first step is to identify the current level so we can prepare the next level accordingly
            if(level.getLevelID().equals("1E"))
            {
                e2.PrepareWindow(this);

                // Save level in another variable
                level = e2;   
            }
            else if(level.getLevelID().equals("2E"))
            {
                e3.PrepareWindow(this);

                // Save level in another variable
                level = e3;  
            }
        }    
    }//GEN-LAST:event_checkBtnActionPerformed

